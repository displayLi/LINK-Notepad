const express = require('express')
const mongoose = require('mongoose')

const Schema = mongoose.Schema;

const Users = new Schema({
    userName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
})

mongoose.model('Users', Users)